package main

func main() {
	example := 55

	//math
	println(12 + 1)
	println(12 - 2)
	println(12 * 2)
	println(12 / 2)
	println(12 % 5)

	//augmented assigments
	example += 1
	//-=, *=, /=, %=
	println(example)

	//unary operator
	example++
	//--
	println(example)
	println(+example)
	println(-example)
	println(!true)
}
